/*【例4-10】输出菲波那契（Fibonacci）序列：1，1，2，3，5，8，…的前10项。*/

/* 输出菲波那契序列：1，1，2，3，5，8，…的前10项 */
#include <stdio.h>
int main(void)
{
    int i, x1, x2, x;  	/* x1和x2依次代表前两项，x表示其后一项 */

    x1 = 1; 	        /* 头两项都是1 */
    x2 = 1;
    printf ("%6d%6d", x1, x2 );  /* 先输出头两项 */
    for(i = 1; i <= 8; i++){     /* 循环输出后8项 */
        x = x1 + x2;          	 /* 计算新项 */
        printf("%6d", x); 
        x1 = x2;             	 /* 更新x1和x2，为下一次计算新项x作准备*/
        x2 = x;    
    }

    return 0;
}
